package com.company.ex1;

public class Calculator {

    public static int addCalc(int value1, int value2) {
        return value1 + value2;
    }

    public static int subCalc(int value1, int value2) {
        return value1 - value2;
    }

    public static int mulCalc(int value1, int value2) {
        return value1 * value2;
    }

    public static int divCalc(int value1, int value2) {
        return value1 / value2;
    }


    public static double addCalc(double value1, double value2) {
        return value1 + value2;
    }

    public static double subCalc(double value1, double value2) {
        return value1 - value2;
    }

    public static double multCalc(double value1, double value2) {
        return value1 * value2;
    }

    public static double divCalc(double value1, double value2) {
        return value1 / value2;
    }
}
